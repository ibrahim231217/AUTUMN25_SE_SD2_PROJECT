const mongoose = require('mongoose');
require('dotenv').config(); 

const mongo_url = process.env.MONGO_CONN ;

mongoose.connect(mongo_url )
    .then(() => {
        console.log("MongoDB Connected !i!i!i!.......");
    })
    .catch((err) => {
        console.error('Error connecting to MongoDB!!! :', err);
    });     


   